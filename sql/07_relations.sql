WITH course_ranks AS (
    SELECT
        c.course_name AS course_name, 
        c.category AS category,
        COUNT (e.enrollment_id) AS students_count,
        RANK() OVER (ORDER BY (COUNT (e.enrollment_id)) DESC) AS course_rank
    FROM courses c JOIN enrollments e
        ON c.course_id = e.course_id
    GROUP BY c.course_id, c.course_name, c.category
)
SELECT
    course_name,
    category,
    students_count
FROM course_ranks
WHERE course_rank = 1;

