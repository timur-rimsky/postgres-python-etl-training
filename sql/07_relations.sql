SELECT 
    s.full_name,
    s.city,
    c.course_name,
    c.category,
    c.price,
    e.enrollment_date,
    e.status
FROM enrollments e
JOIN students s ON e.student_id = s.student_id
JOIN courses c ON e.course_id = c.course_id
ORDER BY s.full_name, c.course_name;

SELECT 
    c.course_name,
    c.category,
    c.price,
    COUNT (e.enrollment_id) AS students_count
FROM courses c
LEFT JOIN enrollments e 
    ON c.course_id = e.course_id
GROUP BY c.course_name, c.category, c.price
ORDER BY students_count DESC;

