WITH course_students AS (
    SELECT
        c.course_name AS course_name, 
        c.category AS category,
        COUNT (e.enrollment_id) AS students_count
    FROM courses c LEFT JOIN enrollments e
        ON c.course_id = e.course_id
    GROUP BY c.course_id, c.course_name, c.category
)
SELECT
    course_name,
    category,
    students_count,
    RANK() OVER (ORDER BY students_count DESC) AS rank_value,
    DENSE_RANK() OVER (ORDER BY students_count DESC) AS dense_rank_value,
    ROW_NUMBER() OVER (ORDER BY students_count DESC, course_name) AS row_number_value
FROM course_students
ORDER BY students_count DESC, course_name;






